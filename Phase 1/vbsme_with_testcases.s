		  addi $s7, $zero, 0               

while: 	  j findSAD	

# load current frame and window data 	
next:	  lw $s2, 8($a0)         
		  lw $s3, 12($a0)        
		  sub $t5, $t0, $s5
		  bgez $t5, right
		  add $s6, $zero, $t3     
		  add $s7, $zero, $t4     
		  add $s5, $zero, $t0    

# traverse right in frame
right: 	  slti $t5, $s4, 2        
		  beq $t5, $zero, down    
		  addi $t4, $t4, 1       
		  add $t6, $t4, $s3      
		  sub $t5, $t6, $s1
		  blez $t5, check
		  addi $s4, $zero, 2     
		  addi $s1, $s1, -1      
		  addi $t4, $t4, -1    
		  j check                

# traverse down in frame
down:	  slti $t5, $s4, 3       
		  beq $t5, $zero, left    
		  addi $t3, $t3, 1       
		  add $t6, $t3, $s2     
		  sub $t5, $t6, $s0
		  blez $t5, check
		  addi $s4, $zero, 3    
		  addi $s0, $s0, -1     
		  addi $t3, $t3, -1     
		  j check               

