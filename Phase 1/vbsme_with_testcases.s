left:	  slti $t5, $s4, 4       
		  beq $t5, $zero, up    
		  addi $t4, $t4, -1      
		  sub $t5, $t4, $t1
		  bgez $t5, check
		  addi $s4, $zero, 4      
		  addi $t1, $t1, 1     
		  addi $t4, $t4, 1   
